import java.util.*;
public class Operators {
    public static void main(String args[]){
        int a=1;
        int b=2;
        int c =a>>>1;
        System.out.println("a+b="+(a+b));
        System.out.println("a-b="+(a-b));
        System.out.println("a*b="+(a*b));
        System.out.println("a/b="+(a/b));
        System.out.println("a%b="+(a%b));
        System.out.println("a&&b="+(a==1 && b==2));
        System.out.println("a||b="+(a==1 || b==2));
        System.out.println("!a="+!(a==1));
        System.out.println("a>>>1="+c);
        
    }
}
